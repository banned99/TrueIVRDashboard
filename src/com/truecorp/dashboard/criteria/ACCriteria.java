package com.truecorp.dashboard.criteria;

public class ACCriteria {
	private String acNo;
	private String acName;
	private String acProductNo;
	private String acProductName;
	private String display;
	
	public String getAcNo() {
		return acNo;
	}
	public void setAcNo(String acNo) {
		this.acNo = acNo;
	}
	public String getAcName() {
		return acName;
	}
	public void setAcName(String acName) {
		this.acName = acName;
	}
	public String getAcProductNo() {
		return acProductNo;
	}
	public void setAcProductNo(String acProductNo) {
		this.acProductNo = acProductNo;
	}
	public String getAcProductName() {
		return acProductName;
	}
	public void setAcProductName(String acProductName) {
		this.acProductName = acProductName;
	}
	public String getDisplay() {
		return display;
	}
	public void setDisplay(String display) {
		this.display = display;
	}
}
